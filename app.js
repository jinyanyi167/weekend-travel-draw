  function haversineKm(a, b) {
    const radius = 6371;
    const dLat = ((b.lat - a.lat) * Math.PI) / 180;
    const dLon = ((b.lon - a.lon) * Math.PI) / 180;
    const lat1 = (a.lat * Math.PI) / 180;
    const lat2 = (b.lat * Math.PI) / 180;
    const sinLat = Math.sin(dLat / 2);
    const sinLon = Math.sin(dLon / 2);
    const h = sinLat * sinLat + Math.cos(lat1) * Math.cos(lat2) * sinLon * sinLon;
    return 2 * radius * Math.asin(Math.sqrt(h));
  }

  function estimateDriveHours(start, dest) {
    if (isSamePlace(start, dest)) return 0;
    const km = haversineKm(start, dest);
    return km * 1.23 / 82 + 0.25;
  }

  function estimateRailHours(start, dest) {
    if (isSamePlace(start, dest)) return 0;
    const startKey = railKeyByCity[start.city] || start.id;
    const override = railOverrides[startKey] && railOverrides[startKey][dest.id];
    if (override) return override;
    const km = haversineKm(start, dest);
    return km / 210 + 0.65;
  }

  function getTravelHours(start, dest, mode) {
    if (mode === "drive2" || mode === "drive4") return estimateDriveHours(start, dest);
    if (mode === "rail4") return estimateRailHours(start, dest);
    if (mode === "flight") return haversineKm(start, dest) / 720 + 2.4;
    return estimateRailHours(start, dest);
  }

  function getTravelWindow(mode) {
    if (mode === "drive2") return { min: 0.75, max: 2 };
    if (mode === "drive4") return { min: 2, max: 4 };
    if (mode === "flight") return { min: 0, max: 6.5 };
    if (mode === "any") return { min: 0, max: 99 };
    return { min: 0, max: 4 };
  }

  function isSamePlace(start, dest) {
    return dest.name === start.city || dest.name === start.fullName || dest.name.replace(/市$/, "") === start.city;
  }

  function scoreDestination(dest, settings) {
    const start = departures.find((item) => item.id === settings.startCity) || departures[0];
    const mode = settings.travelMode || "drive2";
    const hours = getTravelHours(start, dest, mode);
    const travelWindow = getTravelWindow(mode);
    const inRange = (hours >= travelWindow.min && hours <= travelWindow.max) || isSamePlace(start, dest);
    if (settings.strictRange && !inRange && !settings.surpriseMode) return null;

    const qualityFit = 0.82 + Math.min(18, Math.max(0, dest.weekendScore - 70)) / 100;
    const dayFit = Number(settings.tripDays) === 2 && dest.cost >= 4 ? 0.9 : 1;
    let score = 100 * qualityFit * dayFit;
    if (!inRange) score *= settings.surpriseMode ? 0.28 : 0.12;
    if ((settings.visited || []).includes(dest.id)) score *= Number(settings.visitedPenalty || 0.25);
    if (isSamePlace(start, dest)) score *= 0.04;
    return {
      dest,
      score: Math.max(0.1, score),
      hours,
      inRange,
      reasons: buildReasons(dest, settings, hours, inRange),
    };
  }

  function buildReasons(dest, settings, hours, inRange) {
    return [
      `${dest.province}${dest.name}：${dest.summary}`,
      "本次抽签更强调随机性，只保留交通范围、去过降权和惊喜位这些硬条件。",
      `${inRange ? "交通范围内" : "作为惊喜位保留"}，当前口径约 ${hours.toFixed(1)} 小时。`,
    ];
  }

  function getCandidates(settings) {
    const start = departures.find((item) => item.id === settings.startCity) || departures[0];
    const scored = destinations
      .map((dest) => scoreDestination(dest, settings))
      .filter(Boolean)
      .sort((a, b) => b.score - a.score);
    const primary = scored.filter((item) => !isSamePlace(start, item.dest));
    if (!settings.strictRange || settings.surpriseMode || primary.length >= 8) return primary.length ? primary : scored;

    const seen = new Set(primary.map((item) => item.dest.id));
    const supplements = destinations
      .filter((dest) => !seen.has(dest.id) && !isSamePlace(start, dest))
      .map((dest) => {
        const hours = getTravelHours(start, dest, settings.travelMode || "drive2");
        const distance = haversineKm(start, dest);
        const score = Math.max(0.1, 100 * Math.max(0.28, 1 - distance / 1800));
        return {
          dest,
          score,
          hours,
          inRange: false,
          reasons: [
            `${dest.province}${dest.name}：${dest.summary}`,
            "当前出发地周边候选偏少，系统把它作为最近的区域补充备选加入。",
            `区域补充备选，当前口径约 ${hours.toFixed(1)} 小时。`,
          ],
        };
      })
      .sort((a, b) => haversineKm(start, a.dest) - haversineKm(start, b.dest))
      .slice(0, 8 - primary.length);
    const combined = [...primary, ...supplements].sort((a, b) => b.score - a.score);
    return combined.length ? combined : scored;
  }

  function weightedPick(candidates, random = Math.random) {
    if (!candidates.length) return null;
    const total = candidates.reduce((sum, item) => sum + item.score, 0);
    let cursor = random() * total;
    for (const item of candidates) {
      cursor -= item.score;
      if (cursor <= 0) return item;
    }
    return candidates[candidates.length - 1];
  }

  function formatHours(hours, mode) {
    const prefix = mode === "drive2" || mode === "drive4" ? "自驾" : mode === "flight" ? "综合交通" : "高铁/城际";
    if (hours < 0.2) return "本城轻度出逃";
    return `${prefix}约 ${hours.toFixed(1)} 小时`;
  }

