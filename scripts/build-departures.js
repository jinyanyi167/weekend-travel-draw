const fs = require("fs");
const https = require("https");
const path = require("path");

const sourcePath = "/private/tmp/china-areas.json";
const provinces = JSON.parse(fs.readFileSync(sourcePath, "utf8")).features
  .map((feature) => feature.properties)
  .filter((item) => item && item.level === "province");

const directAdmin = new Set([110000, 120000, 310000, 500000, 710000, 810000, 820000]);
const municipalityNames = {
  110000: "北京",
  120000: "天津",
  310000: "上海",
  500000: "重庆",
  710000: "台湾",
  810000: "香港",
  820000: "澳门",
};

function requestJson(url) {
  return new Promise((resolve, reject) => {
    https
      .get(url, (response) => {
        if (response.statusCode !== 200) {
          reject(new Error(`${response.statusCode} ${url}`));
          response.resume();
          return;
        }
        let body = "";
        response.setEncoding("utf8");
        response.on("data", (chunk) => {
          body += chunk;
        });
        response.on("end", () => resolve(JSON.parse(body)));
      })
      .on("error", reject);
  });
}

function stripSuffix(name) {
  return name
    .replace(/特别行政区$/, "")
    .replace(/地区$/, "")
    .replace(/自治州$/, "")
    .replace(/盟$/, "")
    .replace(/市$/, "")
    .replace(
      /(土家族苗族|哈尼族彝族|布依族苗族|壮族苗族|傣族景颇族|藏族羌族|蒙古族藏族|柯尔克孜族|维吾尔族|哈萨克族|朝鲜族|蒙古族|傈僳族|布依族|土家族|哈尼族|景颇族|阿昌族|德昂族|纳西族|普米族|独龙族|东乡族|撒拉族|裕固族|锡伯族|鄂温克族|鄂伦春族|达斡尔族|塔吉克族|乌孜别克族|塔塔尔族|俄罗斯族|仫佬族|毛南族|仡佬族|门巴族|珞巴族|基诺族|保安族|赫哲族|柯尔克孜|维吾尔|哈萨克|朝鲜|蒙古|傈僳|布依|土家|哈尼|景颇|阿昌|德昂|纳西|普米|独龙|东乡|撒拉|裕固|锡伯|鄂温克|鄂伦春|达斡尔|塔吉克|乌孜别克|塔塔尔|俄罗斯|仫佬|毛南|仡佬|门巴|珞巴|基诺|保安|赫哲|满|回|藏|彝|白|傣|壮|苗|侗|瑶|黎|羌|怒|佤|水|畲)+$/,
      ""
    );
}

function toId(adcode, name) {
  return `cn-${adcode}-${name.replace(/[^\u4e00-\u9fa5a-zA-Z0-9]/g, "")}`;
}

(async () => {
  const departures = [];

  for (const province of provinces) {
    if (directAdmin.has(province.adcode)) {
      const center = province.center || province.centroid;
      departures.push({
        id: toId(province.adcode, province.name),
        city: municipalityNames[province.adcode] || stripSuffix(province.name),
        fullName: province.name,
        province: province.name,
        adcode: province.adcode,
        lat: center[1],
        lon: center[0],
      });
      continue;
    }

    const url = `https://geo.datav.aliyun.com/areas_v3/bound/${province.adcode}_full.json`;
    const data = await requestJson(url);
    for (const feature of data.features) {
      const item = feature.properties;
      if (!item || item.level !== "city") continue;
      const center = item.center || item.centroid;
      if (!center) continue;
      departures.push({
        id: toId(item.adcode, item.name),
        city: stripSuffix(item.name),
        fullName: item.name,
        province: province.name,
        adcode: item.adcode,
        lat: center[1],
        lon: center[0],
      });
    }
  }

  departures.sort((a, b) => a.adcode - b.adcode);

  const output = `// Generated by scripts/build-departures.js from Alibaba DataV administrative boundary data.\n(function (root) {\n  \"use strict\";\n\n  const departures = ${JSON.stringify(departures, null, 2)};\n\n  if (typeof module !== \"undefined\" && module.exports) module.exports = departures;\n  root.ChinaDepartures = departures;\n})(typeof window !== \"undefined\" ? window : globalThis);\n`;

  const outDir = path.join(__dirname, "..", "data");
  fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(path.join(outDir, "departures.js"), output);
  console.log(`Generated ${departures.length} departure cities.`);
})();
